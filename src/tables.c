/* 16x16 and 32x32 use cosine basis generated; we provide a compact form. */
static void make_dct_matrix(int n, int16_t out[/*n*n*/]) {
    for (int k = 0; k < n; k++)
        for (int j = 0; j < n; j++) {
            double v = 64.0 * cos((2*j+1) * k * 3.14159265358979 / (2.0*n));
            /* round and store */
            out[k*n+j] = (int16_t)(v >= 0 ? (int16_t)(v + 0.5) : -(int16_t)(-v + 0.5));
        }
}

/* We need static storage for t16/t32. Use init-once via function. */
static int16_t g_t16[16*16];
static int16_t g_t32[32*32];
static int g_dct_inited = 0;

static void ensure_dct(void) {
    if (g_dct_inited) return;
    make_dct_matrix(16, g_t16);
    make_dct_matrix(32, g_t32);
    g_dct_inited = 1;
}

const int16_t avs2_t16[16][16] = { {0} };
const int16_t avs2_t32[32][32] = { {0} };

/* accessor used by itx.c: returns pointer to the generated matrices */
const int16_t *avs2_get_t16(void) { ensure_dct(); return g_t16; }
const int16_t *avs2_get_t32(void) { ensure_dct(); return g_t32; }

